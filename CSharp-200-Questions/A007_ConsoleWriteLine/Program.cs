namespace A007_ConsoleWriteLine
{
    internal class Program
    {
        static void Main(string[] args)
        {
            bool b = true;
            char c = 'A';
            decimal d = 1.234m;     // m은 decimal 형의 접미사
            double e = 1.23456789;      // f는 float 형의 접미사
            float f = 1.23456789f;
            int i = 1234;
            string s = "Hello";

            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
            Console.WriteLine(i);
            Console.WriteLine(s);
            
        }
    }
}
